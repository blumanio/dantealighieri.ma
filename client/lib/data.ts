export const parseDeadlineDateString = (dateString?: string | null, academicYearStartForCourse?: number): Date | null => {
  if (!dateString || typeof dateString !== 'string' || dateString.trim() === "") return null;

  const currentGlobalYear = new Date().getUTCFullYear();
  const resolvedAcademicYearStart = academicYearStartForCourse || currentGlobalYear;

  const monthNames: { [key: string]: number } = {
    'january': 0, 'jan': 0, 'gennaio': 0, 'gen': 0,
    'february': 1, 'feb': 1, 'febbraio': 1, 'febr': 1,
    'march': 2, 'mar': 2, 'marzo': 2,
    'april': 3, 'apr': 3, 'aprile': 3,
    'may': 4, 'maggio': 4, 'mag': 4,
    'june': 5, 'jun': 5, 'giugno': 5, 'giu': 5,
    'july': 6, 'jul': 6, 'luglio': 6, 'lug': 6,
    'august': 7, 'aug': 7, 'agosto': 7, 'ago': 7,
    'september': 8, 'sep': 8, 'settembre': 8, 'set': 8, 'sett': 8,
    'october': 9, 'oct': 9, 'ottobre': 9, 'ott': 9,
    'november': 10, 'nov': 10, 'novembre': 10,
    'december': 11, 'dec': 11, 'dicembre': 11, 'dic': 11
  };

  const dateStrNormalized = dateString.trim().toLowerCase().replace(/,/g, '');
  const parts = dateStrNormalized.split(/\s+/);
  let day: number | undefined;
  let monthStr: string | undefined;

  parts.forEach(part => {
    const num = parseInt(part);
    if (!isNaN(num) && num >= 1 && num <= 31) {
      day = num;
    } else if (monthNames[part] !== undefined) {
      monthStr = part;
    }
  });

  if (day === undefined || monthStr === undefined) {
    const d = new Date(dateString);
    if (!isNaN(d.getTime()) && d.getUTCFullYear() > 1900 && d.getUTCDate() > 0) {
      return new Date(Date.UTC(d.getUTCFullYear(), d.getUTCMonth(), d.getUTCDate(), 23, 59, 59));
    }
    console.warn(`[DateParser] Could not parse day/month from: "${dateString}"`);
    return null;
  }

  const monthIndex = monthNames[monthStr];
  if (monthIndex === undefined) {
    console.warn(`[DateParser] Unknown month string: "${monthStr}" in "${dateString}"`);
    return null;
  }

  let year = resolvedAcademicYearStart;
  if (monthIndex < 7) {
    year = resolvedAcademicYearStart + 1;
  }

  const potentialDate = new Date(Date.UTC(year, monthIndex, day, 23, 59, 59));
  if (isNaN(potentialDate.getTime())) {
    console.warn(`[DateParser] Constructed invalid date: ${year}-${monthIndex + 1}-${day} from "${dateString}"`);
    return null;
  }

  const today = new Date();
  today.setUTCHours(0, 0, 0, 0);
  if (potentialDate.getTime() < today.getTime() && (today.getTime() - potentialDate.getTime()) > (180 * 24 * 60 * 60 * 1000)) {
    potentialDate.setUTCFullYear(potentialDate.getUTCFullYear() + 1);
  }
  return potentialDate;

}

